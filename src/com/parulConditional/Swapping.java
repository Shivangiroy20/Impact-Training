package com.parulConditional;

public class Swapping {	
	

	public static void main(String[] args) {
		int  num1 = 2;
		int num2 = 4;		
		int temp;
		
			temp = num1;
			num1 = num2;
			num2 = temp;
			
		
		System.out.println("The number after swapping"  + num1);
		System.out.println("the number after swapping" + num2);
		
			
	}


	}
